import React from "react";

type IconMap = (0 | 1)[][];

interface IconProps extends React.SVGAttributes<SVGElement> {
  map: IconMap;
  color: string;
}

function Icon({ map, color, ...otherProps }: IconProps) {
  return (
    <svg
      viewBox="0 0 160 160"
      xmlns="http://www.w3.org/2000/svg"
      aria-hidden="true"
      fill={color}
      {...otherProps}
    >
      {map.map((row, y) =>
        row.map((cell, x) => (
          <React.Fragment key={`${y}${x}`}>
            {cell === 1 && (
              <rect x={x * 10} y={y * 10} width={10} height={10} />
            )}
          </React.Fragment>
        ))
      )}
    </svg>
  );
}

export function IconGithub(props: Omit<IconProps, "map">) {
  return <Icon {...props} map={githubMap} />;
}

export function IconVideo(props: Omit<IconProps, "map">) {
  return <Icon {...props} map={videoMap} />;
}

export function IconImage(props: Omit<IconProps, "map">) {
  return <Icon {...props} map={imageMap} />;
}

export function IconRd(props: Omit<IconProps, "map">) {
  return <Icon {...props} map={imageMap} />;
}

